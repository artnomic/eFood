const RestaurantPage = () => {
  const { id } = useParams<{ id: string }>()
  const restaurante = restaurantes.find((r) => r.id === Number(id))

  if (!restaurante) {
    return (
      <>
        <Header variant="default" />
        <ErrorMensage>
          Desculpe, esse restaurante não foi encontrado
        </ErrorMensage>
      </>
    )
  }

  const produtosDoRestaurante = comidas.filter(
    (produto) => produto.restaurantId === restaurante.id
  )

  return (
    <>
      <Header variant="default" />
      <Banner
        title={restaurante.title}
        image={restaurante.image}
        segment={restaurante.segment}
      />
      <ProductList produtos={produtosDoRestaurante} />
    </>
  )
}

export default RestaurantPage
